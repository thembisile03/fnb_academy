# Creating a string
greeting = "Hello ekasi"

# Printing the string
print(greeting)

# String concatenation (joining strings)
name = "Thembi"
welcome_message = greeting + ", " + name + "!"
print(welcome_message)

# String formatting
formatted = f"{greeting}, {name}. Welcome emakasana!"
print(formatted)

# Changing case
print(greeting.upper())  # HELLO EKASI
print(greeting.lower())  # hello ekasi

# String length
print(len(greeting))  # Counts how many characters

# Checking if a word is in the string
print("ekasi" in greeting)  # True

